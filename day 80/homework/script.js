// შექმენით ფუნქციები: დაალაგოთ თქვენი კოდი ფუნქციების გამოყენებით.
// გამოიყენოთ ციკლები და პირობები: გამოიყენეთ while ციკლი, რომ გააგრძელოთ თამაში სანამ სწორ რიცხვს გამოიცნობთ. გამოიყენეთ if პირობა, რომ შეამოწმოთ, სწორია თუ არა გამოცნობილი რიცხვი.
// ყოველი გამოცნობის შემდეგ თქვენ უნდა უთხრათ მომხარებელს, მათი გმოცნობილი საიდუმლო რიცხვზე უფრო მაღალია თუ დაბალი.

function guessSecretNumber() {
    const secretNumber = Math.floor(Math.random() * 100) + 1; 
    let attempts = 0;
    
    console.log("მოგესალმებით! გამოიცანით რიცხვი 1-დან 100-მდე.");
    
    while (true) {
        let guess = parseInt(prompt("შეიყვანეთ თქვენი რიცხვი:"));
        attempts++;
        
        if (isNaN(guess)) {
            console.log("გთხოვთ შეიყვანოთ სწორი რიცხვი!");
            continue;
        }

        if (guess < secretNumber) {
            console.log("საიდუმლო რიცხვი უფრო მაღალია!");
        } else if (guess > secretNumber) {
            console.log("საიდუმლო რიცხვი უფრო დაბალია!");
        } else {
            console.log(`გილოცავთ! თქვენ გამოიცანით რიცხვი ${secretNumber} ${attempts} ცდაში.`);
            break;
        }
    }
}

guessSecretNumber();

